def count_vowels(s):
    vowels = "aeiouAEIOU"  # –í—Ä–∞—Ö–æ–≤—É—î–º–æ —è–∫ –º–∞–ª–µ–Ω—å–∫—ñ, —Ç–∞–∫ —ñ –≤–µ–ª–∏–∫—ñ –≥–æ–ª–æ—Å–Ω—ñ
    count = sum(1 for char in s if char in vowels)  # –ü—ñ–¥—Ä–∞—Ö–æ–≤—É—î–º–æ –≥–æ–ª–æ—Å–Ω—ñ
    return count

# –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞
