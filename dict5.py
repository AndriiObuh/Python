# –û—Å—å –¥–µ–∫—ñ–ª—å–∫–∞ —Å–ø–æ—Å–æ–±—ñ–≤ –≤–∏—Ä—ñ—à–µ–Ω–Ω—è —Ü—ñ—î—ó –∑–∞–¥–∞—á—ñ:
# ‚úÖ 1. –í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è –º–Ω–æ–∂–∏–Ω–∏ set() (—à–≤–∏–¥–∫–∏–π —Å–ø–æ—Å—ñ–±, –∞–ª–µ –Ω–µ –∑–±–µ—Ä—ñ–≥–∞—î –ø–æ—Ä—è–¥–æ–∫)

def merge_lists(lst1, lst2):
    return list(set(lst1) | set(lst2))  # –û–±'—î–¥–Ω–∞–Ω–Ω—è –º–Ω–æ–∂–∏–Ω

print(merge_lists([1, 2, 3], [3, 4, 5]))  
# [1, 2, 3, 4, 5] (–ø–æ—Ä—è–¥–æ–∫ –º–æ–∂–µ –∑–º—ñ–Ω–∏—Ç–∏—Å—è)
# üõ† –ü–æ—è—Å–Ω–µ–Ω–Ω—è:

# set(lst1) | set(lst2) ‚Äì –æ–±'—î–¥–Ω–∞–Ω–Ω—è –¥–≤–æ—Ö –º–Ω–æ–∂–∏–Ω.
# list(...) ‚Äì –ø–µ—Ä–µ—Ç–≤–æ—Ä—é—î –º–Ω–æ–∂–∏–Ω—É –Ω–∞–∑–∞–¥ —É —Å–ø–∏—Å–æ–∫.
# ‚úÖ 2. –í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è dict.fromkeys() (–∑–±–µ—Ä—ñ–≥–∞—î –ø–æ—Ä—è–¥–æ–∫)

def merge_lists(lst1, lst2):
    return list(dict.fromkeys(lst1 + lst2))  # –í–∏–¥–∞–ª—è—î –¥—É–±–ª—ñ, –∑–±–µ—Ä—ñ–≥–∞—é—á–∏ –ø–æ—Ä—è–¥–æ–∫

print(merge_lists([1, 2, 3], [3, 4, 5]))  
# [1, 2, 3, 4, 5]
# üõ† –ü–æ—è—Å–Ω–µ–Ω–Ω—è:

# lst1 + lst2 ‚Äì –æ–±'—î–¥–Ω—É—î –¥–≤–∞ —Å–ø–∏—Å–∫–∏ –≤ –æ–¥–∏–Ω.
# dict.fromkeys(...) ‚Äì —Å—Ç–≤–æ—Ä—é—î —Å–ª–æ–≤–Ω–∏–∫, –∑–∞–ª–∏—à–∞—é—á–∏ —Ç—ñ–ª—å–∫–∏ —É–Ω—ñ–∫–∞–ª—å–Ω—ñ –∫–ª—é—á—ñ (—Ç–æ–±—Ç–æ —É–Ω—ñ–∫–∞–ª—å–Ω—ñ —á–∏—Å–ª–∞ —É –ø—Ä–∞–≤–∏–ª—å–Ω–æ–º—É –ø–æ—Ä—è–¥–∫—É).
# list(...) ‚Äì –ø–æ–≤–µ—Ä—Ç–∞—î —Ä–µ–∑—É–ª—å—Ç–∞—Ç —É –≤–∏–≥–ª—è–¥—ñ —Å–ø–∏—Å–∫—É.
# ‚úÖ 3. –í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è —Ü–∏–∫–ª—É (–µ—Ñ–µ–∫—Ç–∏–≤–Ω–æ –¥–ª—è –∑–±–µ—Ä–µ–∂–µ–Ω–Ω—è –ø–æ—Ä—è–¥–∫—É)

def merge_lists(lst1, lst2):
    merged = []
    for num in lst1 + lst2:
        if num not in merged:
            merged.append(num)
    return merged

print(merge_lists([1, 2, 3], [3, 4, 5]))  
# [1, 2, 3, 4, 5]
