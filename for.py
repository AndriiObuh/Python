# Реалізуй цикл, який виводить всі прості числа до 100.

# for i in range(1, 100):
#     print(i)

# Твій код просто виведе всі числа від 1 до 99, але не відфільтрує прості числа.
# Ось правильний варіант для знаходження всіх простих чисел до 100:

def is_prime(n):
    if n < 2:
        return False
    for i in range(2, int(n ** 0.5) + 1):
        if n % i == 0:
            return False
    return True

for i in range(1, 100):
    if is_prime(i):
        print(i, end=" ")  # Виведе всі прості числа у рядок
# Пояснення:
# Функція is_prime(n) перевіряє, чи є число n простим:
# Якщо число менше 2, воно не є простим.
# Перевіряємо подільність на всі числа від 2 до √n.
# Якщо хоч один дільник знайдено, число не просте.
# Цикл for i in range(1, 100):
# Викликає is_prime(i), щоб перевірити, чи є число простим.
