def invert_dict_safe(dct):
    inverted = {}
    for key, value in dct.items():
        if value in inverted:
            inverted[value].append(key)  # –î–æ–¥–∞—î–º–æ —â–µ –æ–¥–∏–Ω –∫–ª—é—á —É —Å–ø–∏—Å–æ–∫
        else:
            inverted[value] = [key]  # –°—Ç–≤–æ—Ä—é—î–º–æ —Å–ø–∏—Å–æ–∫ —ñ–∑ –ø–µ—Ä—à–∏–º –∫–ª—é—á–µ–º
    return inverted

print(invert_dict_safe({"a": 1, "b": 2, "c": 1}))  
# {1: ['a', 'c'], 2: ['b']}
