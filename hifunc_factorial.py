# üîπ –í–∞—Ä—ñ–∞–Ω—Ç 1: –†–µ–∫—É—Ä—Å—ñ—è

def factorial(n):
    if n == 0 or n == 1:  # –ë–∞–∑–æ–≤–∏–π –≤–∏–ø–∞–¥–æ–∫
        return 1
    return n * factorial(n - 1)  # –†–µ–∫—É—Ä—Å–∏–≤–Ω–∏–π –≤–∏–∫–ª–∏–∫

# –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞
print(factorial(5))  # 120
print(factorial(3))  # 6
print(factorial(0))  # 1
print(factorial(7))  # 5040
