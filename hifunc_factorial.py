# üîπ –í–∞—Ä—ñ–∞–Ω—Ç 2: –ß–µ—Ä–µ–∑ —Ü–∏–∫–ª for (—ñ—Ç–µ—Ä–∞—Ç–∏–≤–Ω–∏–π –º–µ—Ç–æ–¥)

def factorial(n):
    result = 1
    for i in range(2, n + 1):
        result *= i
    return result

# –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞
print(factorial(5))  # 120
