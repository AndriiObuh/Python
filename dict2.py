# Напиши функцію, яка приймає список імен та повертає словник, де ключ — ім'я, а значення — його довжина.

def names_length(names):
    return {name: len(name) for name in names}

# Приклад використання
names = ["Анна", "Богдан", "Катерина"]
result = names_length(names)
print(result)  # {'Анна': 4, 'Богдан': 6, 'Катерина': 8}
# Пояснення коду:
# Використання словникового виразу (dict comprehension):
# {name: len(name) for name in names}
# Для кожного імені у списку створюється ключ name і значення len(name).
# Приклад роботи функції:
# "Анна" → 4
# "Богдан" → 6
# "Катерина" → 8
