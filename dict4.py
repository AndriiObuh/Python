# –û—Å—å –ø—Ä–∞–≤–∏–ª—å–Ω–∏–π –≤–∞—Ä—ñ–∞–Ω—Ç, —è–∫–∏–π –∑–±–µ—Ä—ñ–≥–∞—î –ø–æ—Ä—è–¥–æ–∫ —É–Ω—ñ–∫–∞–ª—å–Ω–∏—Ö –∑–Ω–∞—á–µ–Ω—å:

def unique_values(lst):
    return list(dict.fromkeys(lst))

print(unique_values([1, 2, 2, 3, 4, 4, 5]))  
